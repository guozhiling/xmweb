    <div class="title-1">连串过关</div>
                 <p style="margin-left:15px">在连串过关中有任何的投注赛事无效或者打和（如以下的范例），此连串过关注单仍然有效，并且按照任何所胜出的其余投注结算，范例如下：</p>
		              <ul style="margin-left:10px">
            					<li>投注项1：切尔西（-0.5）-切尔西 赢。 </li>
                      <li>投注项2:投注2：曼联（-1）-曼联赢 1-0. </li>
                      <li>投注项 3:投注3: 阿森纳（-0.5）-阿森纳赢。 </li>                  
    </ul> 	
           <p style="margin-left:15px">正如曼联是以（-1）的亚洲盘口1-0获胜，但在连串过关中的此赛事视为和。因此，当切尔西获胜连串阿森纳获胜过关，此连串过关将被视为切尔西以及阿森纳的2串，而非最初的3串。同时，打和的过关投注项目将会被自动以1计算。</p>
           <p style="margin-left:30px">过关的计算范例如下 </p>
