						<h1>连串过关</h1>
    <h5>示意</h5>
<div>
	<p style="margin-left:15px">连串过关是指选择二个或更多的赛事在一个单一的注单中。每一个选择连串投注的赛事必须获胜此连串的注单才视为获胜。如果第一个注单是获胜的，投注获胜的注单会添加到第二个投注选项，直到连串过关中的所有投注获胜或到有一场失败为结束。  </p>
            <p style="margin-left:30px">例如： </p>
            <p style="margin-left:40px">投注一个100元的3串1连串过关在以下的三场赛事: </p>
               <ul style="margin-left:20px">
     						 <li>曼联@ 1.80</li>
                        <li>切尔西@ 1.50 </li>
                        <li>阿森纳@ 1.66 </li>
                  	  </ul> 			
            
            <p style="margin-left:40px">如果所有的三场赛事都获胜，连串过关的盈利为448.20元（包括本金）。详细的计算方式您可以查看以下的信息:</p>
               <ul style="margin-left:20px">
            						<li>注单1：曼联1.80*$100=返还$180. </li>
                        <li>注单2：切尔西1.50*$180=返还$270.   </li>
                        <li>注单3：阿森纳 1.66*$270=返还$448.20.  </li>
 	  </ul> 			
            
             <p style="margin-left:40px">Parlay Points:</p>
               <ul style="margin-left:20px">
            						<li>本公司一个注单中最多的连串过关为10串。  </li>
                        <li>所有投注赛事都需要根据体育博彩规则为准。 </li>
                   			<li>不是所有的赛事都可以投注连串过关。如果您在投注列表中看到不可以组合二个不相关的赛事（可以查看以下的信息关于有关联的连串），那么就是其中一场并没有开出连串过关投注。  </li>
                        <li>连串过关可以投注不同种类的赛事，以及不在同一天的比赛。 </li>
 	  </ul> 			
            
		</ul>
		<div class="title-1">连串过关中选项</div>
    <p style="margin-left:15px">连串过关投注，选择组合有关联的同一赛事或投注市场的结果会影响其他另一个投注市场，此注单是不接受的。例如:</p>
                		<p style="margin-left:30px">例如: </p>
            <p style="margin-left:40px">以下的2串1是不接受的，由于是同一场赛事: </p>
    <ul style="margin-left:20px">
            						<li>曼联获胜独赢盘口@1.80 </li>
   											<li>曼联2-0获胜，正确比分盘口@7.0 </li>
                        </ul> 	
           <p style="margin-left:15px">如果曼联2-0获胜，组合盘口为12.6。其实盘口应该为7.0，因为曼联2-0获胜，那么独赢盘口自然而然为获胜。 </p>
           <p style="margin-left:15px">连串过关投注，选择组合有关联的同一球队或球员，即使他们是不同的时间，同样是不接受例如一个结果会影响另一个结果。</p> 
           <p style="margin-left:30px">例如: </p>
           			<ul style="margin-left:20px">
            					<li>曼联进入到最后的冠军杯决赛@6.0 </li>
                      <li>曼联进入到最后的冠军杯决赛@6.0 </li>
                      <li>组合盘口@60.0 </li>
 	 	</ul>
           <p style="margin-left:15px">这个连串过关被视为第二个赛果会影响到第一个赛果。如果曼联获得冠军杯联赛冠军，那么曼联自然而然就进入冠军杯决赛。因此，盘口仅仅为10.0。 </p> 
           <p style="margin-left:15px">本公司有权利取消有关联的连串过关投注。</p>      
          
